-- 2. Product Performance
-- Evaluate how well products are performing in terms of sales and revenue.


-- 1. How many products exist in each category?
SELECT c.cat_name AS "CATEGORY",count(p.prod_name) AS "COUNT" FROM categories c 
LEFT JOIN products p ON
c.cat_id=p.cat_id
GROUP BY 1 ORDER BY 2 ;


-- 2. What is the average price of products by category?
SELECT c.cat_name AS "CATEGORY",avg(p.price) AS "AVERAGE" ,sum(p.price) AS "TOTAL" FROM categories c 
left JOIN products p ON
c.cat_id=p.cat_id
GROUP BY 1 ORDER BY 2,3 desc ;

-- 3. Which products have the highest total sales volume (by quantity)?


SELECT p.prod_name,o.each_price,sum(o.quantity) as quantity,sum(o.total_price)as Total
from products p  join order_details o 
on p.prod_id=o.prod_id
group by 1,2 order by Total desc limit 1;

-- 4. What is the total revenue generated by each product?

SELECT p.prod_name,o.each_price,sum(o.quantity) as quantity,sum(o.total_price)as "Total Revenue"
from products p  join order_details o 
on p.prod_id=o.prod_id
group by 1,2 order by "Total Revenue" desc ;


