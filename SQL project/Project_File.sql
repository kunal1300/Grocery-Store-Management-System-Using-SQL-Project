USE PROJECT;

-- 1. Supplier Table
CREATE TABLE IF NOT EXISTS supplier (
    sup_id TINYINT AUTO_INCREMENT PRIMARY KEY,
    sup_name VARCHAR(255),
    address TEXT
);
select * from supplier;

-- 2. Categories Table
CREATE TABLE IF NOT EXISTS categories (
    cat_id TINYINT AUTO_INCREMENT PRIMARY KEY,
    cat_name VARCHAR(255)
);

select * from categories;

-- 3. Employees Table
CREATE TABLE IF NOT EXISTS employees (
    emp_id TINYINT AUTO_INCREMENT PRIMARY KEY,
    emp_name VARCHAR(255),
    hire_date VARCHAR(255)
);
select * from employees;


-- 4. Customers Table
CREATE TABLE IF NOT EXISTS customers (
    cust_id SMALLINT AUTO_INCREMENT PRIMARY KEY,
    cust_name VARCHAR(255),
    address TEXT
);


select * from customers;


-- 5. Products Table
CREATE TABLE IF NOT EXISTS products (
    prod_id TINYINT AUTO_INCREMENT PRIMARY KEY,
    prod_name VARCHAR(255),
    sup_id TINYINT,
    cat_id TINYINT,
    price DECIMAL(10,2),
    CONSTRAINT fk_products_supplier
        FOREIGN KEY (sup_id)
        REFERENCES supplier(sup_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_products_categories
        FOREIGN KEY (cat_id)
        REFERENCES categories(cat_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);
select * from products;


-- 6. Orders Table
CREATE TABLE IF NOT EXISTS orders (
    ord_id SMALLINT AUTO_INCREMENT PRIMARY KEY,
    cust_id SMALLINT,
    emp_id TINYINT,
    order_date VARCHAR(255),
    CONSTRAINT fk_orders_customers
        FOREIGN KEY (cust_id)
        REFERENCES customers(cust_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_orders_employees
        FOREIGN KEY (emp_id)
        REFERENCES employees(emp_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

select * from orders;

-- 7. Order_Details Table
CREATE TABLE IF NOT EXISTS order_details (
    ord_det_id SMALLINT AUTO_INCREMENT PRIMARY KEY,
    ord_id SMALLINT,
    prod_id TINYINT,
    quantity TINYINT,
    each_price DECIMAL(10,2),
    total_price DECIMAL(10,2),
    CONSTRAINT fk_orderdetails_orders
        FOREIGN KEY (ord_id)
        REFERENCES orders(ord_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_orderdetails_products
        FOREIGN KEY (prod_id)
        REFERENCES products(prod_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);


-- 1. Customer Insights
-- 1 How many unique customers have placed orders?
SELECT count(DISTINCT(cust_id))AS unique_customers
FROM orders;

-- 2 Which customers have placed the highest number of orders?

SELECT c.cust_name 
	AS "Customer Name",count(o.cust_id) AS "Total Orders"  
FROM customers c 
LEFT JOIN orders o 
	ON c.cust_id=o.cust_id
GROUP BY 1 
ORDER BY 2 
DESC LIMIT 1;


-- 3 What is the total and average purchase value per customer? 

SELECT c.cust_name as " NAME ",sum(total_price) as "TOTAL PRICE",avg(total_price) as "AVERAGE"
FROM customers c 
left JOIN orders o 
	ON c.cust_id=o.cust_id
INNER JOIN order_details od 
on od.ord_id =o.ord_id
group by 1 order by 2,3;

-- 4 Who are the top 5 customers by total purchase amount?

SELECT c.cust_name as " NAME ",sum(total_price) as "TOTAL PRICE"
FROM customers c 
left JOIN orders o 
	ON c.cust_id=o.cust_id
INNER JOIN order_details od 
on od.ord_id =o.ord_id
group by 1 order by 2 desc limit 5;

